# Function to get meal suggestions based on time of day
def get_meals_by_time(time_of_day, limit=10):
    """Get meal suggestions based on time of day"""
    suitable_meals = []
    for meal in MEAL_DATASET:
        suitable_for = meal.get("suitable_for", [])
        # Now we can just check if time_of_day is in the list
        if time_of_day in suitable_for:
            suitable_meals.append(meal)
    return suitable_meals[:limit]

# Function to get meal suggestions based on tags
def get_meals_by_tags(tags, limit=10):
    """Get meal suggestions based on tags"""
    suitable_meals = []
    for meal in MEAL_DATASET:
        meal_tags = meal.get("tags", [])
        # Now we can just check if any tag is in the list
        if any(tag in meal_tags for tag in tags):
            suitable_meals.append(meal)
    return suitable_meals[:limit]

