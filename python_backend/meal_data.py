# Convert dataset to standardized format
def standardize_meal_dataset(dataset):
    """
    Standardizes the meal dataset by ensuring 'suitable_for' and 'ingredients' are always lists
    and converts all entries to a consistent format
    """
    for meal in dataset:
        # Convert suitable_for to list if it's a string
        if isinstance(meal.get('suitable_for'), str):
            # Split by semicolon or comma, depending on what's in the string
            if ';' in meal['suitable_for']:
                meal['suitable_for'] = [item.strip() for item in meal['suitable_for'].split(';')]
            else:
                meal['suitable_for'] = [item.strip() for item in meal['suitable_for'].split(',')]
        
        # Convert ingredients to list if it's a string
        if isinstance(meal.get('ingredients'), str):
            # Split by semicolon or comma, depending on what's in the string
            if ';' in meal['ingredients']:
                meal['ingredients'] = [item.strip() for item in meal['ingredients'].split(';')]
            else:
                meal['ingredients'] = [item.strip() for item in meal['ingredients'].split(',')]
        
        # Convert tags to list if it's a string
        if isinstance(meal.get('tags'), str):
            meal['tags'] = [item.strip() for item in meal['tags'].split(',')]
        
        # Convert id to string format consistently for all entries
        if isinstance(meal.get('id'), int):
            meal['id'] = str(meal['id'])
    
    return dataset

